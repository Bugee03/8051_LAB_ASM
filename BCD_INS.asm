ORG 0000H

            MOV     R0,#40H     ; R0 = указатель на массив BCD чисел
            MOV     R2,#5        ; R2 = количество чисел (5 штук)

            CLR     A            ; A = 0 (накопитель суммы)
            MOV     R7,#00H      ; R7 = вспомогательный регистр (для переносов)

AGAIN:      MOV     A,R7         ; восстановить A после переносов
            ADD     A,@R0        ; добавить следующий BCD байт
            DA      A            ; корректировать в BCD

            JNC     NEXT         ; если нет переноса — пропустить обновление R7
            INC     R7           ; если перенос (CY=1) → увеличить старший байт (R7)

NEXT:       INC     R0           ; следующий элемент массива
            DJNZ    R2,AGAIN     ; уменьшить счетчик, повторять пока не 0

            ; После выполнения:
            ; R7 = старший байт суммы (десятки, сотни...)
            ; A  = младший байт суммы (единицы)